            <h3>Պահպանում</h3>
            <p>&nbsp &nbsp Մենք պահպանում ենք ձեր տեղեկությունները այնքան ժամանակ, որքան անհրաժեշտ է՝ մեզնից
                պահանջված ծառայությունները տրամադրելու համար, կամ այնքան ժամանակ, որքան ողջամտորեն 
                անհրաժեշտ է պահպանել դրանք մեր օրինական բիզնես նպատակների համար, օրինակ՝ օգտվելու մեր 
                օրինական իրավունքներից կամ որտեղ մեզ թույլատրված է դա անել։<br>
                &nbsp Խնդրում ենք կապնվել մեզ հետ՝ օգտագործելով ստորև բերված «Կապ մեզ հետ» բաժնի 
                տեղեկատվությունը, եթե ցանկանում եք մանրամասն տեղեկություններ ստանալ ձեր տարբեր տեսակի 
                անձնական տվյալների մեր պահպանման ժամկետների մասին:</p>
            <h3>Ձեր իրավունքները՝ տեղեկատվության հասանելիություն և ճշգրտություն, թարմացում, ուղղում կամ
