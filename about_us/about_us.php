            <h3>Օրինական շահեր</h3>
            <p>&nbsp &nbsp  Ինչպես նշվեց վերևում, որոշակի հանգամանքներում մենք կարող ենք օգտագործել ձեր անձնական
                տվյալները մեր օրինական շահերը կամ երրորդ անձանց օրինական շահերը առաջ մղելու համար:<br>
